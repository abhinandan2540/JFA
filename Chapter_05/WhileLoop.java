package Chapter_05;

public class WhileLoop {
    public static void main(String[] args) {

//        Example 1
        int num=1;
        while(num <=10){
            System.out.println(num);
            num++;
        }

//        Example 2
        int num1 =100;
        while (num1<=200){
            System.out.println(num1);
            num1++;
        }

    }
}
